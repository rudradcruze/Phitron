#include <stdio.h>
int main()
{

    int test;
    scanf("%d", &test);
    for (int i = 1; i <= test; i++)
    {
        for (int j = 0; j < i; j++)
        {
            printf("*");
        }
        printf("\n");
    }

    return 0;
}